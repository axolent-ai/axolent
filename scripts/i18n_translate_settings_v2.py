def main() -> int:
    updated_count = 0
    errors = []

    for lang_code, translations in TRANSLATIONS.items():
        locale_file = LOCALES_DIR / f"{lang_code}.json"
        if not locale_file.exists():
            errors.append(f"{lang_code}.json not found")
            continue

        with open(locale_file, "r", encoding="utf-8") as f:
            data = json.load(f)

        keys_section = data.get("keys", {})
        lang_updated = 0

        for key in SETTINGS_KEYS:
            if key not in keys_section:
                errors.append(f"{lang_code}: key '{key}' not found in locale file")
                continue

            if key not in translations:
                errors.append(f"{lang_code}: no translation provided for '{key}'")
                continue

            entry = keys_section[key]
            # Update text and mark as reviewed
            entry["text"] = translations[key]
            entry["auto_translated"] = False
            entry["reviewed"] = True
            # Keep source_hash unchanged (it references the EN source)
            lang_updated += 1

        if lang_updated > 0:
            with open(locale_file, "w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
                f.write("\n")
            print(f"{lang_code}: updated {lang_updated}/{len(SETTINGS_KEYS)} keys")
            updated_count += lang_updated

    print(f"\nDone. Total updates: {updated_count}")
    if errors:
        print(f"\nErrors ({len(errors)}):")
        for err in errors:
            print(f"  - {err}")
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
